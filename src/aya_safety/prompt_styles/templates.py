def get_cipher_template(cipher_style: str, target_language: str) -> str:
    """
    Get the pre-translated cipher template.

    Args:
        cipher_style: One of "ascii", "atbash", "caesar", "morse"
        target_language: ISO language code (ar, bn, de, es, fr, hi, ja, ru, zh)
                        or "en" for English original

    Returns:
        The complete template string in the target language

    Raises:
        KeyError: If cipher_style or target_language not found
    """
    return CIPHER_TEMPLATES[cipher_style][target_language]

def replace_cipher_placeholders(template: str, cipher_examples: list[str]) -> str:
    """
    Replace placeholder markers in template with actual cipher examples.

    Args:
        template: Template string with [CIPHER_TYPE_CODE_PLACEHOLDER] markers
        cipher_examples: List of 3 cipher code blocks for examples 0, 1, 2

    Returns:
        Template with placeholders replaced by actual cipher content
    """
    result = template
    for i, example in enumerate(cipher_examples):
        placeholder = f"[{template.split('_')[0].upper()}_CODE_PLACEHOLDER]"
        result = result.replace(placeholder, example, 1)
    return result